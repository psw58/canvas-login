						<h2>Implement Technology</h2>
						<p>Teaching fields are consistently ranked highly against peer institutions, and the University is consistently ranked in the top 15th among research universities worldwide. Today, our Center for Teaching Innovation is an intellectually-stimulating, warm, vibrant community of nearly 100 fields of study.</p>
				
						<h3>Engage Your Students</h3>
						<p>Teaching study has been a part of the Cornell experience since the founding of the University in 1865.  In fact, Cornell is one of the first institutions of higher education in the United States to offer advanced degrees, and one of the very first to admit women and African American students.  Today, our Center for Teaching Innovation is an intellectually-stimulating, warm, vibrant community of nearly 100 fields of study.</p>
						<blockquote class="offset">
