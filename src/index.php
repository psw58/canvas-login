						<p>By design, Teaching study at Cornell is cross-disciplinary and integrative.  Our students experience considerable flexibility, freedom, and independence in their academic programs.  Students take an active role in defining their degree programs under the guidance of a faculty committee, called the Special Committee. Committee members may be chosen from more than 1,800 faculty members across Cornell’s departments, colleges, and campuses.</p>
